    public void TampilData() {
        DftTblModel_mapel = new DefaultTableModel();
        DftTblModel_mapel.addColumn("KODE MAPEL");
        DftTblModel_mapel.addColumn("NAMA MAPEL");
        DftTblModel_mapel.addColumn("NAMA JURUSAN");
        tableMapel.setModel(DftTblModel_mapel);
        Connection conn = new koneksi().connect();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM tb_mapel";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_mapel.addRow(new Object[]{
                    res.getString("kode_mapel"),
                    res.getString("nama_mapel"),
                    res.getString("kode_jurusan")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
    public void dataFromDataBaseToComboBox(){
        Connection conn = new koneksi().connect();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM tb_jurusan";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            
            listJurusan.removeAllItems();
            listJurusan.addItem("--- PILIH JURUSAN MAPEL ---");
            while (res.next()) {
                listJurusan.addItem(res.getString("nama_jurusan"));
            }
            
            res.last();
            int jumlahData = res.getRow();
            res.first();
            
        } catch (SQLException e) {
        }
    }
    
