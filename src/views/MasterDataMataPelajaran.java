    private void tableMapelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMapelMouseClicked
        // TODO add your handling code here:
        int baris = tableMapel.getSelectedRow();
        txtKodeMapel.setText(DftTblModel_mapel.getValueAt(baris, 0).toString());
        txtNamaMapel.setText(DftTblModel_mapel.getValueAt(baris, 1).toString());
        String comboSub = DftTblModel_mapel.getValueAt(baris, 2).toString();
        for (int i = 0; i < listJurusan.getItemCount(); i++) {
            if (listJurusan.getItemAt(i).toString().equalsIgnoreCase(comboSub)) {
                listJurusan.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tableMapelMouseClicked

