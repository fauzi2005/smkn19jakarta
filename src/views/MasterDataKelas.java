    protected void datatable(){
    Object [] Baris = {"ID KELAS","KELAS","ID JURUSAN","KELOMPOK KELAS"};
    tabmode = new DefaultTableModel(null, Baris);
    tabelkelas.setModel(tabmode);
    String cari = tcari.getText();            
    try {
    String sql = "Select * from tabel_kelas where kelas like '%"+cari+"%' "
            + "or id_jurusan like '%"+cari+"%' or kelompok_kelas like '%"+cari+"%' "
            + "ORDER BY id_kelas";
    
        java.sql.Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while (hasil.next()){
            String a = hasil.getString("id_kelas");
            String b = hasil.getString("kelas");
            String c = hasil.getString("id_jurusan");
            String d = hasil.getString("kelompok_kelas");
            
            String[] data={a,b,c,d};
            tabmode.addRow(data);
        }
    }catch (Exception e){
        }
    }
    
